// 🌟 2. Caching

// Definition:
// Caching is the process of storing frequently accessed data in a fast storage layer so that future requests can be served quickly.

// Think of it as keeping a copy of your favorite snacks on the desk instead of going to the kitchen every time.

// caching is the process of storing frequently accesed data in a fast storage layer so that future requests can be served quickly 


// 1️⃣ Why Caching?

// Benefits:

// Faster response times – reduces latency for users
// faster response times reducces latency for users 
// Reduces load on the main database or backend
// reduces load on the main database or backend 

// Improves scalability – handles more traffic without slowing down

// Example:

// Website shows trending products → instead of querying the database every time, store the list in cache

// User requests → served directly from cache → super fast

// Common cache types:

// In-memory (RAM) → Redis, Memcached

// Browser cache → HTML, CSS, JS

// CDN cache → images, videos

// 2️⃣ Cache Invalidation

// Definition:
// Process of removing outdated or stale data from cache to ensure accuracy.
// proccess of removing outdated or stale data from cache to ensure accuracy
// Key Strategies:

// Time-based expiration (TTL – Time To Live)
// time-based expiration 

// Data automatically expires after a set time

// Example: Trending products cache expires every 5 minutes

// Manual invalidation

// Explicitly remove/update cache when data changes

// Example: Update product details → clear cache for that product

// Write-through / Write-behind caching

// Write-through: Update cache and database at the same time

// Write-behind: Update cache first → asynchronously update database

// Problem to remember:

// Cache is fast, but stale cache = wrong data

// Cache invalidation is the hardest problem in computer science (classic interview joke!)

// 3️⃣ Redis Basics

// Redis = Remote Dictionary Server

// In-memory key-value store → super fast

// Supports data structures: strings, hashes, lists, sets, sorted sets

// Often used for caching, session storage, and queues

// Key Features:

// In-memory → microsecond read/write

// Persistence options → optional disk storage (RDB/AOF)

// TTL support → automatic expiration of keys

// Pub/Sub → message broadcasting

// Example Usage:

// SET user:123 "John" EX 300    # cache expires in 5 minutes
// GET user:123                   # fetch from cache


// Common Use Cases:

// Website session storage

// API response caching

// Leaderboards / counters

// Queues (task queues in background jobs)

// 📝 Quick Interview Summary

// Caching → speed up reads, reduce backend load

// Cache invalidation → TTL, manual update, write-through/write-behind

// Redis → in-memory, key-value store, supports TTL, fast






